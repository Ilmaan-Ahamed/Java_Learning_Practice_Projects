public class Operators {
    public static void main(String[] args) {
        // Arithmetic Operators + - / *

        int a = 10;
        int b =20;
        System.out.println(a+b); // + , - , / , *
        System.out.println();


        // Relational Operators < , > , ==
        int e =10;
        int f =20;
        System.out.println(e<f); 
        System.out.println();


        // Logical Operators AND , OR 
        boolean p =true;
        boolean o =false;
        System.out.println(p || o); // && = AND , || = OR




    }
}
