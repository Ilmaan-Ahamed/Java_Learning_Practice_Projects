// Print 10 Numbers from an Array using for loop

class CC3p2q2 {
    public static void main(String[] args) {
        
    int[] Number ={14,58,74,95,65,57,58,48,74,25};
    System.out.println(Number);

    for(int i=0; i<=9; i=i+1)
    {
        System.out.println(Number[i]);
    }
    }
}
